/**
 * https://leetcode.com/problems/reverse-string/
 * @param {character[]} s
 * @return {void} Do not return anything, modify s in-place instead.
 */
var reverseString = function (s) {
  return s.reverse()
}

const s = ['H', 'a', 'n', 'n', 'a', 'h']
const result = reverseString(s)
console.log(result)
